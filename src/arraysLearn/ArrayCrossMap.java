package arraysLearn;

public class ArrayCrossMap {

	public static void main(String[] args) {
	
		int b[]=new int[100];
		
		int bb[]=new int[100];
		
		
		
		
		
		
		
		
		for(int i=0;i<b.length;i++)
		{
			b[i]=i*2;
		}
		
		
		
		
		
		
		for(int i=0;i<b.length;i++)
		{
			bb[i]=b[i];
		}
		
		
		for(int ab:bb)
		{
			System.out.println(ab);
		}
		
		
		char ch[]=new char[6];
		
		for(char c:ch)
		{
			System.out.println(c);
			
		}
		
		
		
		
		

	}

}
